using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AbstractFactory
{
    public class Finish
    {
        public float x;
        public float y;

        public Finish()
        {
            x = 0;
            y = 0;

        }
        public Finish(float _x, float _y)
        {
            x = _x;
            y = _y;
        }
    }
}
